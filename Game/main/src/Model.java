	private void gameLogic() { 
		
		// this is a way to increment across the array list data structure 
		
		//If Player has ended current gameloop, reset world
		if(player.getReset()) {
//			this.Rooms = new CopyOnWriteArrayList<Room>(); 
			//this.rooms.generateRooms(2, player);
			this.rooms.generateRooms(((int)(Math.random() * 8)) + 7, player);
			this.player.resetPlayer();
		}
		
		if(player.getCanSave()) {
			this.SavePlayer(this.player);
			this.SaveRooms(this.rooms);
			player.setCanSave(false);
		}
		
		//Check if Rooms are cleared and key has not spawned
		if(allRoomsCleared() && !(rooms.isKeySpawned())) {
			//Pick a random room (prob not start)
			Room temp = rooms.getRoomArray()[(int)(Math.random() * (rooms.getRoomArray().length - 1)) + 1];
			//Spawn key?
			temp.spawnItem("Key", 225, 255);
			rooms.setKeySpawned(true);
			sounds.playSound("keySpawn");
		}
		
		//Check if Player has the key and treasure room is not open
		if(player.hasKey() && !(rooms.isTreasureOpen())) {
			//Pick a random room (prob not start)
			Room temp = rooms.getRoomArray()[(int)(Math.random() * (rooms.getRoomArray().length - 1)) + 1];
			//Spawn treasure entrance
			temp.addTreasureEntrance();
			this.rooms.setTreasureEntranceRoom(temp);
			this.rooms.setTreasureOpen();
		}

		
		//see if they hit anything 
		// using enhanced for-loop style as it makes it alot easier both code wise and reading wise too 
		for (Enemy temp : rooms.getCurrentRoom().getEnemiesList()) 
		{
			for (Bullet bullet : rooms.getCurrentRoom().getBulletList()) 
			{	
				//Enemy Hit
				if ( (bullet.getCollider().intersects(temp.getCollider())) && (bullet.isFriendly()))
				{
					
					//Save last enemy killed location
					int x = (int) temp.getBody().getCentre().getX();
					int y = (int) temp.getBody().getCentre().getY();
					
					temp.setHealth(temp.getHealth() - 50);
					rooms.getCurrentRoom().getBulletList().remove(bullet);
					
					//Handle removal
					if(temp.getHealth() <= 0) {
						sounds.playSound("splat");
						rooms.getCurrentRoom().getEnemiesList().remove(temp);
						rooms.getCurrentRoom().setTotalEnemies(rooms.getCurrentRoom().getTotalEnemies()-1);
						
						//Spawn item chance on kill (not final enemy)
						if(rooms.getCurrentRoom().getTotalEnemies() > 0) {
							if(Math.random() < (0.25 + player.getLuck())) {
								if(Math.random() < 0.5) {
									rooms.getCurrentRoom().spawnItem("Coin", x, y);
								} else {
									rooms.getCurrentRoom().spawnItem("Heart", x, y);
								}
							}
						}
					}
					
					//If no enemies left
					if(rooms.getCurrentRoom().getTotalEnemies() == 0) {
						
						if(rooms.getCurrentRoom() == rooms.getTreasureRoom()) {
							rooms.getTreasureRoom().spawnRandomTreasureItem(400, 400, rooms.getStartRoom());
						} else {
							//Spawn item (25% chance)
							if(Math.random() < 0.25) {
								rooms.getCurrentRoom().spawnRandomItem(x, y);
							}
						}
					}
				}  
			}
		}
		
		//If player collects item
		if(rooms.getCurrentRoom().hasItem()) {
			for(int i = 0; i < rooms.getCurrentRoom().getItems().size(); i++) {
				if(player.getCollider().intersects(rooms.getCurrentRoom().getItem(i).getCollider())) {
					//Pick up item if not store
					if(!rooms.getCurrentRoom().getType().equals("STORE")) {
						rooms.getCurrentRoom().getItem(i).useAbility(player);
						rooms.getCurrentRoom().removeItem(i);
					} else {
						//Check buy cooldown
						if((System.currentTimeMillis() - player.getLastPurchaseTime()) > 2000) { 
							//Charge for item if store
							if(player.getStoredCoins() >= rooms.getCurrentRoom().getItem(i).getCost()) {
								player.setStoredCoins(player.getStoredCoins()-rooms.getCurrentRoom().getItem(i).getCost());
								rooms.getCurrentRoom().getItem(i).useAbility(player);
								//rooms.getCurrentRoom().removeItem(i);
								player.setLastPurchaseTime(System.currentTimeMillis());
								sounds.playSound("coinSpent");
							}
						}
					}
				}
			}
		}
		
		//If player dies
		if(player.getHealth() <= 0) {
			player.resetPlayer();
			rooms.setCurrentRoom(rooms.getStartRoom());
			player.getBody().getCentre().setX(500);
			player.getBody().getCentre().setY(500);
			//this.Rooms = new CopyOnWriteArrayList<Room>(); 
			//this.rooms.generateRooms(2, player);
			this.rooms.generateRooms(((int)(Math.random() * 8)) + 7, player);
			sounds.playSound("death");
		}
		
	}

	private void enemyLogic() {
		for (Enemy temp : rooms.getCurrentRoom().getEnemiesList()) 
		{	
			temp.move(player, rooms.getCurrentRoom());
			temp.attack(player, rooms.getCurrentRoom());
		}
		
		if(rooms.getCurrentRoom().getTotalEnemies() > 0) {
			while((rooms.getCurrentRoom().getEnemiesList().size() < rooms.getCurrentRoom().getMaxEnemies()) && (rooms.getCurrentRoom().getEnemiesList().size() < rooms.getCurrentRoom().getTotalEnemies())) {
				rooms.getCurrentRoom().getEnemiesList().add(rooms.getCurrentRoom().getEnemyManager().getRandom());
				rooms.getCurrentRoom().getEnemiesList().get(rooms.getCurrentRoom().getEnemiesList().size()-1).spawn(rooms.getCurrentRoom(), player, rooms);
			}
		}
	}

	private void bulletLogic() {
		// TODO Auto-generated method stub
		// move bullets 
	  
		for (Bullet temp : rooms.getCurrentRoom().getBulletList()) 
		{
		    //check to move them
			
			temp.move(rooms);
			//see if they hit anything 
			
			//see if they get to the edge of the screen
			if ((temp.getBody().getCentre().getY() < -10) || (temp.getBody().getCentre().getY() > 810) || (temp.getBody().getCentre().getX() < -10) || (temp.getBody().getCentre().getX() > 810))
			{
			 	rooms.getCurrentRoom().getBulletList().remove(temp);
			} 
			
			//Player Hit
			if ( (temp.getCollider().intersects(player.getCollider())) && (!(temp.isFriendly())))
			{
				rooms.getCurrentRoom().getBulletList().remove(temp);
				player.setHealth(player.getHealth()-10);
			}
			
			if(this.start == false) {
				//Check start room start button hit
				if(rooms.getCurrentRoom() == rooms.getStartRoom()) {
					if(temp.getCollider().intersects(rooms.getStartRoom().getStartButton().getCollider())) {
						this.start = true;
						rooms.getCurrentRoom().getBulletList().remove(temp);
						rooms.getCurrentRoom().getLand().getBody().setTexture("res/Den2.png");
					}
				}
				
				//Check start room load button hit
				if(rooms.getCurrentRoom() == rooms.getStartRoom()) {
					if(temp.getCollider().intersects(rooms.getStartRoom().getLoadButton().getCollider())) {
						this.start = true;
						LoadPlayer();
						LoadRooms();
						rooms.getCurrentRoom().getBulletList().remove(temp);
						rooms.getCurrentRoom().getLand().getBody().setTexture("res/Den2.png");
					}
				}
			}
		} 
		
	}
	
	private void gasLogic() {
		// TODO Auto-generated method stub
		// move bullets 
	  
		for (Gas temp : rooms.getCurrentRoom().getGasList()) 
		{
		    //check to move them
			
			//Player Hit
			if ( (temp.getCollider().intersects(player.getCollider())))
			{
				//Damage player
				if((System.currentTimeMillis() - temp.getCreationTime()) % 20 == 0) {
					player.setHealth(player.getHealth()-1);
				}
			}
			
			//Time out
			if((System.currentTimeMillis() - temp.getCreationTime()) > 5000) {
				rooms.getCurrentRoom().getGasList().remove(temp);
			}
		} 
		
	}

